// Reads the REAL source files and emits src/generated/snippets.ts so the
// teaching UI always shows the exact code that runs. Run by predev/prebuild.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(scriptDir, "..");
const repoRoot = resolve(webRoot, "../..");

// id -> { file (repo-relative), label, lang }
const SOURCES = {
  networks: {
    file: "packages/x402-core/src/networks.ts",
    label: "x402-core/networks.ts",
    lang: "typescript",
  },
  requirements: {
    file: "packages/x402-core/src/x402.ts",
    label: "x402-core/x402.ts",
    lang: "typescript",
  },
  cdpJwt: {
    file: "packages/x402-core/src/cdp-jwt.ts",
    label: "x402-core/cdp-jwt.ts",
    lang: "typescript",
  },
  facilitator: {
    file: "packages/x402-core/src/facilitator.ts",
    label: "x402-core/facilitator.ts",
    lang: "typescript",
  },
  merchant: {
    file: "apps/web/src/app/api/merchant/report/route.ts",
    label: "web · api/merchant/report/route.ts",
    lang: "typescript",
  },
  buyer: {
    file: "apps/web/src/hooks/useLiveBuyer.ts",
    label: "web · hooks/useLiveBuyer.ts",
    lang: "typescript",
  },
};

const snippets = {};
for (const [id, { file, label, lang }] of Object.entries(SOURCES)) {
  const code = readFileSync(resolve(repoRoot, file), "utf8").replace(/\s+$/, "") + "\n";
  snippets[id] = { label, lang, code };
}

const outDir = resolve(webRoot, "src/generated");
mkdirSync(outDir, { recursive: true });

const banner = "// AUTO-GENERATED by scripts/extract-snippets.mjs — do not edit by hand.\n";
const body =
  banner +
  "export interface Snippet {\n  label: string;\n  lang: string;\n  code: string;\n}\n\n" +
  `export const SNIPPETS = ${JSON.stringify(snippets, null, 2)} as const satisfies Record<string, Snippet>;\n\n` +
  "export type SnippetId = keyof typeof SNIPPETS;\n";

writeFileSync(resolve(outDir, "snippets.ts"), body);
console.log(`extracted ${Object.keys(snippets).length} snippets -> src/generated/snippets.ts`);
